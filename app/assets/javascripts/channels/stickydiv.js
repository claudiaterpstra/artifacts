function fixDiv() {
    var $div = $("#stickydiv");
    if ($(window).scrollTop() > $div.data("top")) {
        $('#stickydiv').css({'position': 'fixed', 'top': '0', 'width': '100%'});
    }
    else {
        $('#stickydiv').css({'position': 'static', 'top': 'auto', 'width': '100%'});
    }
}

$("#stickydiv").data("top", $("#stickydiv").offset().top); // set original position on load
$(window).scroll(fixDiv);
